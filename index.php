									var campos = $(this).find("input, select, textarea") ;
									var btn = $(this).find("button[type='submit']");
									var btnValue= btn.text();
									btn.prop("disabled",true);
									btn.text("Procesando..");
									$.post($(this).attr("action"), campos.serialize(),function(data){
													if(data.status==true){
														$(".modal").modal("hide");
														campos.val("");
													}
													alert(data.message || ((data.status==false)?"Algo salio mal al procesar la solicitud":"Solicitud realizada"));

													if(typeof data.redirection!="undefined"){
														window.location = data.redirection;
													}
													btn.text(btnValue);
													btn.prop("disabled",false);
									},"JSON").fail(function(){
													alert("Fallo la solicitud");
													btn.text(btnValue);
													btn.prop("disabled",false);
									});
				});
